package tech.niemandkun.opengl.io;

public class Size {
    private final int mWidth;
    private final int mHeight;

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }

    public Size(int width, int height) {
        mWidth = width;
        mHeight = height;
    }
}
